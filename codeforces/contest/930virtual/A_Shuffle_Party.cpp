void solve(){
    int n; cin >> n;

    int ans = 1;
    while ((ans<<1) <= n){
        ans <<= 1;
    }

    cout << ans << '\n';
}

int main(){
    cin.tie(0)->ios_base::sync_with_stdio(false);
    int t; cin >> t;
    while (t--){
        solve();
    }
}